/**
 * Migrate Strapi "pages" content from an old Strapi instance (e.g. VPS)
 * to the current Strapi instance.
 *
 * This focuses on the pages that the frontend loads via getPage():
 *
 *   - diensten      ( /diensten )
 *   - avg           ( /avg )
 *   - bio           ( /bio )
 *   - iso-14001     ( /iso-14001 )
 *   - iso-16175     ( /iso-16175 )
 *   - iso-27001     ( /iso-27001 )
 *   - blog          ( /blog )
 *
 * Usage (from project root):
 *
 *   OLD_STRAPI_URL=http://153.92.223.23:1337 \
 *   OLD_STRAPI_TOKEN=... \
 *   NEW_STRAPI_URL=https://peaceful-insight-production.up.railway.app \
 *   NEW_STRAPI_TOKEN=... \
 *   node scripts/migrate-pages-from-old-strapi.js
 *
 * Notes:
 * - Requires that both old and new Strapi APIs are reachable from where you run this script.
 * - Requires that the provided tokens have permission to read/write the "page" collection type.
 */

const fetch = require('node-fetch');

const {
  OLD_STRAPI_URL = 'http://153.92.223.23:1337',
  OLD_STRAPI_TOKEN,
  NEW_STRAPI_URL,
  NEW_STRAPI_TOKEN,
  STRAPI_URL,
  NEXT_PUBLIC_BACKEND_URL,
} = process.env;

const resolvedNewStrapiUrl =
  NEW_STRAPI_URL ||
  STRAPI_URL ||
  NEXT_PUBLIC_BACKEND_URL ||
  'http://localhost:1337';

if (!OLD_STRAPI_TOKEN) {
  console.error(
    '[migrate-pages] OLD_STRAPI_TOKEN is not set. Please export a token with read access on the old Strapi instance.',
  );
}

if (!NEW_STRAPI_TOKEN) {
  console.error(
    '[migrate-pages] NEW_STRAPI_TOKEN is not set. Please export a token with write access on the new Strapi instance.',
  );
}

const PAGE_SLUGS = [
  'diensten',
  'avg',
  'bio',
  'iso-14001',
  'iso-16175',
  'iso-27001',
  'blog',
];

/**
 * Helper: build headers for old/new Strapi
 */
function buildHeaders(token) {
  const headers = {
    'Content-Type': 'application/json',
  };
  if (token) {
    headers['Authorization'] = `Bearer ${token}`;
  }
  return headers;
}

/**
 * Fetch a page (by slug) from the old Strapi instance
 */
async function fetchOldPage(slug) {
  const url = `${OLD_STRAPI_URL.replace(/\/$/, '')}/api/pages?filters[slug][$eq]=${encodeURIComponent(
    slug,
  )}&populate=*`;

  console.log(`[migrate-pages] Fetching from OLD Strapi: ${url}`);

  const res = await fetch(url, {
    method: 'GET',
    headers: buildHeaders(OLD_STRAPI_TOKEN),
  });

  if (!res.ok) {
    const text = await res.text();
    throw new Error(
      `Old Strapi request failed for slug "${slug}": ${res.status} ${res.statusText} — ${text}`,
    );
  }

  const json = await res.json();
  if (!json.data || !Array.isArray(json.data) || json.data.length === 0) {
    console.warn(
      `[migrate-pages] No page found in OLD Strapi for slug "${slug}".`,
    );
    return null;
  }

  const item = json.data[0];
  console.log(
    `[migrate-pages] Found OLD page for "${slug}" with id=${item.id}.`,
  );
  return item; // { id, attributes: { ... } }
}

/**
 * Fetch an existing page (by slug) from the new Strapi instance
 */
async function fetchNewPage(slug) {
  const url = `${resolvedNewStrapiUrl.replace(
    /\/$/,
    '',
  )}/api/pages?filters[slug][$eq]=${encodeURIComponent(slug)}&populate=*`;

  console.log(`[migrate-pages] Checking NEW Strapi for existing page: ${url}`);

  const res = await fetch(url, {
    method: 'GET',
    headers: buildHeaders(NEW_STRAPI_TOKEN),
  });

  if (!res.ok) {
    const text = await res.text();
    throw new Error(
      `New Strapi lookup failed for slug "${slug}": ${res.status} ${res.statusText} — ${text}`,
    );
  }

  const json = await res.json();
  if (!json.data || !Array.isArray(json.data) || json.data.length === 0) {
    console.log(
      `[migrate-pages] No existing page found in NEW Strapi for slug "${slug}".`,
    );
    return null;
  }

  const item = json.data[0];
  console.log(
    `[migrate-pages] Existing NEW page for "${slug}" has id=${item.id}.`,
  );
  return item;
}

/**
 * Prepare attributes for upsert:
 * - Take attributes from old page
 * - Remove fields that should be generated by Strapi
 */
function buildPageAttributesFromOld(oldPage) {
  const attrs = oldPage.attributes || oldPage || {};

  // Shallow clone to avoid mutating original
  const data = { ...attrs };

  // Ensure slug stays as expected
  if (!data.slug) {
    data.slug = oldPage?.attributes?.slug || oldPage?.slug || '';
  }

  // Remove fields we don't want to forcibly overwrite
  delete data.createdAt;
  delete data.updatedAt;
  delete data.publishedAt;
  delete data.documentId;
  delete data.localizations;

  // Optionally ensure layout is at least an empty array
  if (!Array.isArray(data.layout)) {
    data.layout = data.layout || [];
  }

  return data;
}

/**
 * Create or update the page in NEW Strapi.
 */
async function upsertNewPage(slug, oldPage) {
  const existingNewPage = await fetchNewPage(slug);
  const data = buildPageAttributesFromOld(oldPage);

  if (!data.title && !data.Title) {
    console.warn(
      `[migrate-pages] WARNING: Page "${slug}" has no title in old data. It will still be migrated, but you may want to add a title manually later.`,
    );
  }

  if (existingNewPage) {
    // Update existing entry
    const id = existingNewPage.id;
    const url = `${resolvedNewStrapiUrl.replace(
      /\/$/,
      '',
    )}/api/pages/${id}`;

    console.log(
      `[migrate-pages] Updating existing NEW page id=${id} for slug "${slug}".`,
    );

    const res = await fetch(url, {
      method: 'PUT',
      headers: buildHeaders(NEW_STRAPI_TOKEN),
      body: JSON.stringify({ data }),
    });

    if (!res.ok) {
      const text = await res.text();
      throw new Error(
        `Failed to update NEW page "${slug}" (id=${id}): ${res.status} ${res.statusText} — ${text}`,
      );
    }

    const json = await res.json();
    console.log(
      `[migrate-pages] UPDATED page "${slug}" in NEW Strapi (id=${json.data?.id}).`,
    );
    return json;
  } else {
    // Create a new entry
    const url = `${resolvedNewStrapiUrl.replace(
      /\/$/,
      '',
    )}/api/pages`;

    console.log(`[migrate-pages] Creating NEW page for slug "${slug}".`);

    const res = await fetch(url, {
      method: 'POST',
      headers: buildHeaders(NEW_STRAPI_TOKEN),
      body: JSON.stringify({ data }),
    });

    if (!res.ok) {
      const text = await res.text();
      throw new Error(
        `Failed to create NEW page "${slug}": ${res.status} ${res.statusText} — ${text}`,
      );
    }

    const json = await res.json();
    console.log(
      `[migrate-pages] CREATED page "${slug}" in NEW Strapi (id=${json.data?.id}).`,
    );
    return json;
  }
}

/**
 * Migrate a single slug: fetch from OLD, then upsert into NEW.
 */
async function migrateSlug(slug) {
  console.log('\n==============================================');
  console.log(`[migrate-pages] Migrating slug: "${slug}"`);
  console.log('==============================================\n');

  try {
    const oldPage = await fetchOldPage(slug);
    if (!oldPage) {
      console.warn(
        `[migrate-pages] Skipping "${slug}" because it does not exist in OLD Strapi.`,
      );
      return { slug, status: 'skipped', reason: 'not-found-old' };
    }

    const result = await upsertNewPage(slug, oldPage);

    const hasLayout =
      !!result?.data?.attributes?.layout &&
      Array.isArray(result.data.attributes.layout) &&
      result.data.attributes.layout.length > 0;

    console.log(
      `[migrate-pages] Completed migration for "${slug}". hasLayout=${hasLayout}, layoutItems=${
        hasLayout ? result.data.attributes.layout.length : 0
      }`,
    );

    return {
      slug,
      status: 'migrated',
      id: result.data?.id,
      hasLayout,
      layoutCount: hasLayout ? result.data.attributes.layout.length : 0,
    };
  } catch (error) {
    console.error(
      `[migrate-pages] ERROR while migrating "${slug}":`,
      error.message,
    );
    return { slug, status: 'error', error: error.message };
  }
}

/**
 * Main entry point
 */
async function main() {
  console.log('==============================================');
  console.log(' Strapi Pages Migration (OLD VPS -> NEW STRAPI)');
  console.log('==============================================\n');

  console.log('[migrate-pages] Configuration:');
  console.log(`  OLD_STRAPI_URL:      ${OLD_STRAPI_URL}`);
  console.log(
    `  OLD_STRAPI_TOKEN:    ${OLD_STRAPI_TOKEN ? '[SET]' : '[NOT SET]'}`,
  );
  console.log(`  NEW_STRAPI_URL:      ${resolvedNewStrapiUrl}`);
  console.log(
    `  NEW_STRAPI_TOKEN:    ${NEW_STRAPI_TOKEN ? '[SET]' : '[NOT SET]'}`,
  );
  console.log(`  Slugs to migrate:    ${PAGE_SLUGS.join(', ')}`);
  console.log('');

  if (!OLD_STRAPI_TOKEN || !NEW_STRAPI_TOKEN) {
    console.warn(
      '[migrate-pages] One or both tokens are missing. The script will likely fail until you configure them.',
    );
  }

  const results = [];
  for (const slug of PAGE_SLUGS) {
    // eslint-disable-next-line no-await-in-loop
    const res = await migrateSlug(slug);
    results.push(res);
  }

  console.log('\n==============================================');
  console.log(' Migration summary');
  console.log('==============================================');
  console.log(JSON.stringify(results, null, 2));
  console.log('==============================================\n');
}

main().catch((err) => {
  console.error('[migrate-pages] Fatal error:', err);
  process.exit(1);
});